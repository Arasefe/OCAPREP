package _15.Equality.Primitive;

public class Equality1 {
    public static void main(String[] args) {
        int a=3;
        int b=3;
        System.out.println(a==b);

        int c=5;
        int d=6;
        System.out.println(c==d);

        byte f=3;
        byte g=3;
        System.out.println(f==g);

        System.out.println(a==f);
        System.out.println(b==g);
    }
}
