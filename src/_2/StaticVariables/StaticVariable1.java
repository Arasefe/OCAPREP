package _2.StaticVariables;

public class StaticVariable1 {
    public static byte myByte;
    public static short myShort;
    public static int myInt;
    public static long myLong;
    public static float myFloat;
    public static double myDouble;
    public static char myChar;
    public static boolean myBoolean;

    public static void main(String[] args) {
        System.out.println("My byte = " + myByte);
        System.out.println("My short =" + myShort);
        System.out.println("My int =" + myInt);
        System.out.println("My long =" + myLong);
        System.out.println("My float =" + myFloat);
        System.out.println("My double =" + myDouble);
        System.out.println("My char =" + myChar);
        System.out.println("My boolean =" + myBoolean);
    }
}
